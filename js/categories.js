// 收入分类名集合（用于判断类型）
const INCOME_CAT1 = new Set(Object.keys(PRESET_CATEGORIES.income));

class CategoryManager {
  getCat1List(type) {
    const presets = Object.keys(PRESET_CATEGORIES[type] || {});
    // 自定义分类会从 DB 加载后合并
    return presets;
  }

  getCat2List(cat1, type) {
    return PRESET_CATEGORIES[type]?.[cat1] || [];
  }

  getIcon(cat1) {
    return CATEGORY_ICONS[cat1] || '📌';
  }

  getMatch(cat1, cat2) {
    return [cat1, cat2];
  }

  isIncomeCategory(cat1) {
    return INCOME_CAT1.has(cat1);
  }

  // 关键词按长度降序排列（用于优先匹配长词）
  getSortedKeywords() {
    return Object.keys(KEYWORD_MAP).sort((a, b) => b.length - a.length);
  }

  matchKeyword(text) {
    for (const kw of this.getSortedKeywords()) {
      if (text.includes(kw)) return { keyword: kw, match: KEYWORD_MAP[kw] };
    }
    return null;
  }

  // 自定义分类
  async addCustom(name, parentName, type, icon = '📌') {
    await db.addUserCategory({ name, parentName: parentName || null, type, icon });
  }

  async getCustomCat1(type) {
    const cats = await db.getUserCategories(type);
    return cats.filter(c => !c.parentName).map(c => c.name);
  }

  async getCustomCat2(cat1, type) {
    const cats = await db.getUserCategories(type);
    return cats.filter(c => c.parentName === cat1).map(c => c.name);
  }
}

const catManager = new CategoryManager();
